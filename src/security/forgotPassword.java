    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    String accountId = idfp.getText().trim();
    String email = emailfp.getText().trim();

    // Validate if Account ID and Email are entered
    if (accountId.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter both Account ID and Email!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate if Account ID is numeric
    if (!accountId.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "Invalid Account ID! Please enter a numeric value.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection conn = (Connection) new dbConnector().getConnection();
         PreparedStatement stmt = conn.prepareStatement("SELECT * FROM tbl_registeruser WHERE u_id = ? AND u_email = ?")) {

        stmt.setInt(1, Integer.parseInt(accountId));
        stmt.setString(2, email);
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            // Account exists, proceed to security verification
            int userId = rs.getInt("u_id");
            String status = rs.getString("u_status");

            if (!status.equalsIgnoreCase("Active")) {
                JOptionPane.showMessageDialog(null, "Your account is inactive. Please contact the admin.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            JOptionPane.showMessageDialog(null, "Verification successful! Proceeding to password reset.", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            // Open the change password window
            changePass cp = new changePass(userId);  
            cp.setVisible(true);
            this.dispose(); // Close current window

        } else {
            JOptionPane.showMessageDialog(null, "Account ID and Email do not match!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1MouseClicked

