    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
                                  
    int id = Integer.parseInt(Uid.getText());
    String fname = firstName.getText();
    String lname = lastName.getText();
    String uname = userName.getText();
    String mail = email.getText();
    String pass = password.getText();

    if (fname.isEmpty() || lname.isEmpty() || uname.isEmpty() || mail.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(null, "All fields must be filled!");
        return;
    }

    String sql = "UPDATE tbl_registeruser SET u_fname=?, u_lname=?, u_username=?, u_email=?, u_password=? WHERE u_id=?";

    try {
        dbConnector dbc = new dbConnector();
        Connection conn = (Connection) dbc.getConnection();
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, fname);
        pst.setString(2, lname);
        pst.setString(3, uname);
        pst.setString(4, mail);
        pst.setString(5, pass);
        pst.setInt(6, id);

        int updated = pst.executeUpdate();

        if (updated > 0) {
            JOptionPane.showMessageDialog(null, "User information updated successfully!");

            // Redirect to settings form (replace `changeInfo` with your settings form class name)
            changeInfo settings = new changeInfo();
            settings.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Update failed!");
        }

        pst.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage());
    }



    }//GEN-LAST:event_saveMouseClicked

